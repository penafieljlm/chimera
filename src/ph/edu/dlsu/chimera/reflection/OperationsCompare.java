package ph.edu.dlsu.chimera.reflection;

/**
 * Enumerates comparative operations for the sake of reflection capabilities.
 *
 * @author John Lawrence M. Penafiel <penafieljlm@gmail.com>
 */
public enum OperationsCompare {

    /**
     * Equals operation (==)
     */
    Equals,
    /**
     * Not equals operation (!=)
     */
    NotEquals,
    /**
     * Less than or equals (<=)
     */
    LessThanOrEquals,
    /**
     * Greater than or equals (>=)
     */
    GreaterThanOrEquals,
    /**
     * Less than (<)
     */
    LessThan,
    /**
     * Greater Than (>)
     */
    GreaterThan
}
