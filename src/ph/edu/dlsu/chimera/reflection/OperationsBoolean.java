package ph.edu.dlsu.chimera.reflection;

/**
 * Enumerates boolean operations for the sake of reflection functionalities.
 *
 * @author John Lawrence M. Penafiel <penafieljlm@gmail.com>
 */
public enum OperationsBoolean {

    /**
     * And operation (&&)
     */
    And,
    /**
     * Or operation (||)
     */
    Or,
    /**
     * Xor operation (^^)
     */
    Xor,
    /**
     * Nand operation (!&)
     */
    Nand,
    /**
     * Nor operation (!|)
     */
    Nor,
    /**
     * Xnor operation (!^)
     */
    Xnor
}
